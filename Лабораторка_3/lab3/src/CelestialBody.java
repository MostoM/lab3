public class CelestialBody {
    private String name;
    private double g;

    public void set_name(String name){
        this.name = name;
    }

    public void set_g(double g){
        this.g = g;
    }

    public String get_name(){
        return name;
    }

    public double get_g(){
        return g;
    }
}
